package escapeEx;

public class EscapeEx1 {

	public static void main(String[] args) {
		System.out.println("안녕\n하세요.");
		System.out.println("12345678");
		System.out.println("123\t456");
		System.out.println("12\t45");
		System.out.println("저는 \'홍길동\' 입니다.");
		System.out.println("저는 \"홍길동\" 입니다.");
		System.out.println("저는 \\홍길동\\ 입니다.");
	}

}
